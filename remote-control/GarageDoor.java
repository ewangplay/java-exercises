public class GarageDoor {
	public GarageDoor() {}

	public void up() {
		System.out.println("The garage door is open.");
	}

	public void down() {
		System.out.println("The garage door is close.");
	}

	public void stop() {
		System.out.println("The garage door has been stoped.");
	}

	public void lightOn() {
		System.out.println("The garage door light is on.");
	}

	public void lightOff() {
		System.out.println("The garage door light is off.");
	}
}
